    PRODUCT_NAME STRING,
    SALES FLOAT,
    UNITS INTEGER,
    GROSS_PROFIT FLOAT,
    COST FLOAT,
    SHIPPING_DAYS INTEGER,
    PROFIT_MARGIN FLOAT
