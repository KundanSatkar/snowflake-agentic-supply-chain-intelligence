    ORDER_DATE DATE,
    SHIP_DATE DATE,
    CORRECTED_SHIP_DATE DATE,
    SHIP_MODE STRING,
    CUSTOMER_ID INTEGER,
    COUNTRY_REGION STRING,
    CITY STRING,
    STATE_PROVINCE STRING,
    POSTAL_CODE STRING,
    DIVISION STRING,
    REGION STRING,
